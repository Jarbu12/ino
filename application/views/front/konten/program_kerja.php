            <p><h3>Setiap tahun UCAC merencanakan dan melaksanakan program kerja diantaranya:</h3><br><br>
<h4>1.    Intensive Career Preparation</h4><br>
              <p> Pendidikan singkat non formal yang diberikan guna meningkatkan ketrampilan dan keahlian mahasiswa dan lulusan tertentu sesuai dengan kebutuhan perusahaan (user). Pelatihan ini terdiri dari public speaking, performance, curriculum vitae, technologi information,dan grapich design.</p>
<br>

<h4>2.    Student Internship</h4><br>
<p>Kegiatan mahasiswa/ calon lulusan untuk memperoleh pengalaman kerja (magang) di suatu perusahaan/ instansi.</p>
<br>

<h4>3.    USM Career Expo (UCE)</h4><br>
<p>Kegiatan bursa kerja yang merupakan sarana fisik tempat berkumpulnya pelaku dunia kerja untuk mempromosikan institusi, bidang perusahaan, serta terutama lowongan kerja yang tersedia.</p>
<br>

<h4>4.    Campus Hiring</h4><br>
<p>Pelaksaan seleksi rekrutmen tenaga kerja dalam waktu satu hari (one day service) di dalam kampus meliputi; penerimaan berkas lamaran, walk in interview, psikotest dan lain-lain.,</p>
<br>

<h4>5.    Job Counseling</h4><br>
<p>Kegiatan yang membantu lulusan dalam memilih pekerjaan yang sesuai berupa asesmen serta pemberian saran dan masukan yang bersifat individual.</p>
<br>

<h4>6.    Tracer Study</h4>
<p>Studi pelacakan jejak lulusan yang dilakukan antara 1-3 tahun setelah lulus dan bertujuan untuk mengetahui:</p>
<li>   Outcome pendidikan dalam bentuk transisi dari dunia pendidikan tinggi ke dunia kerja (termasuk masa tunggu kerja dan proses pencarian kerja pertama), situasi kerja terakhir, dan aplikasi kompetensi di dunia kerja.</li>
<li>    Output pendidikan yaitu penilaian diri terhadap penguasaan dan pemerolehan kompetensi.</li>
<li>    Proses pendidikan berupa evaluasi proses pembelajaran dan kontribusi pendidikan tinggi terhadap pemerolehan kompetensi.</li>
<li>    Input pendidikan berupa penggalian lebih lanjut terhadap informasi sosiobiografis lulusan.</li>
<br>
<h4>7.    UCAC Promotion dan Sosialization</h4><br>
Upaya pengenalan pusat karir dan alumni mengenai layanannya kepada perusahaan/ instansi penyedia kerja dan mahasiswa atau lulusan    
